/*
Switch case
*/

#include <stdio.h>

int main(){
    int age = 38;
    switch (age)
    {
    case 20:
        printf(" you are 20 years old");
        break;
    case 38:
        printf(" you are 38 years old");
        break;
    case 40:
        printf(" you are 40 years old");
        break;
    
    default:
        break;
    }
    
    return 0;
}